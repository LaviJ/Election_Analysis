score = int(input("what is the score grade? "))

#Determine the score grade.
if score >= 90:
    print("Your grade is an A")
elif score >=80:
    print("Your grade is an B")
elif score >= 70:
    print("Your grade is an C")
elif score >= 60:
    print("Your grade is an D")
else:
    print("Your grade is an F")





